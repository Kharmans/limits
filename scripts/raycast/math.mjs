/**
 * Minimum.
 * @param {number} x
 * @param {number} y
 * @returns {number}
 * @internal
 * @ignore
 */
export function min(x, y) {
    return x < y ? x : y;
}

/**
 * Maximum.
 * @param {number} x
 * @param {number} y
 * @returns {number}
 * @internal
 * @ignore
 */
export function max(x, y) {
    return x > y ? x : y;
}
